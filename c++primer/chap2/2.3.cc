#include <iostream>
using namespace std;
int main(int argc, char *argv[]) {
  unsigned u = 10, u2 = 42;
  cout << "u2 - u : " << u2 - u << "\n";
  cout << "u - u2 : " << u - u2 << "\n";
  int i = 10, i2 = 42;
  cout << "i2 - i : " << i2 - i << "\n";
  cout << "i - i2 : " << i - i2 << "\n";

  return 0;
}
